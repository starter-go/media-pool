package dxo

import "github.com/starter-go/base/lang"

type ObjectID string

type ObjectPath string

// ObjectSum: sha256sum(object)
type ObjectSum = SHA256SUM

////////////////////////////////////////////////////////////////////////////////

func (id ObjectID) String() string {
	return string(id)
}

////////////////////////////////////////////////////////////////////////////////

func (sum ObjectSum) String() string {
	h := sum.Hex()
	return h.String()
}

func (sum ObjectSum) Hex() lang.Hex {
	b := sum[:]
	return lang.HexFromBytes(b)
}

////////////////////////////////////////////////////////////////////////////////

func (path ObjectPath) String() string {
	return string(path)
}

////////////////////////////////////////////////////////////////////////////////
